    getFragranceImage(perfume) {
        if (!this.isInitialized) this.init();
        
        let imageName = this.imageMap[perfume.name];
        
        if (!imageName) {
            const lowerName = perfume.name.toLowerCase();
            for (const [key, value] of Object.entries(this.imageMap)) {
                if (key.toLowerCase() === lowerName) {
                    imageName = value;
                    break;
                }
            }
        }
        
        if (!imageName) {
            return 'placeholder.avif';
        }
        
        return `photos/${imageName}`;
    }

    getBrandLogo(brand) {
        if (!this.isInitialized) this.init();
        
        return this.brandLogos[brand] || null;
    }

    // Search for fragrance names that contain the query
    searchFragrances(query) {
        if (!this.isInitialized) this.init();
        
        const normalizedQuery = query.toLowerCase();
        const matches = [];
        
        for (const [name, image] of Object.entries(this.imageMap)) {
            if (name.toLowerCase().includes(normalizedQuery)) {
                matches.push({ name, image });
            }
        }
        
        return matches;
    }

    // Get all available fragrance names
    getAllFragranceNames() {
        if (!this.isInitialized) this.init();
        
        return Object.keys(this.imageMap);
    }

    // Get all available brand names
    getAllBrandNames() {
        if (!this.isInitialized) this.init();
        
        return Object.keys(this.brandLogos);
    }
